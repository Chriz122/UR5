class GroupMambaLayer(nn.Module):
    def __init__(self, input_dim, output_dim, d_state=16, d_conv=3, expand=1, reduction=16):
        super().__init__()
        num_channels_reduced = input_dim // reduction
        self.fc1 = nn.Linear(input_dim, num_channels_reduced, bias=True)
        self.fc2 = nn.Linear(num_channels_reduced, output_dim, bias=True)
        self.relu = nn.ReLU()
        self.sigmoid = nn.Sigmoid()
        self.input_dim = input_dim
        self.output_dim = output_dim
        self.norm = nn.LayerNorm(input_dim)

        # SS2D 必須在上下文中定義
        self.mamba_g1 = SS2D(d_model=input_dim // 4, d_state=d_state, ssm_ratio=expand, d_conv=d_conv)
        self.mamba_g2 = SS2D(d_model=input_dim // 4, d_state=d_state, ssm_ratio=expand, d_conv=d_conv)
        self.mamba_g3 = SS2D(d_model=input_dim // 4, d_state=d_state, ssm_ratio=expand, d_conv=d_conv)
        self.mamba_g4 = SS2D(d_model=input_dim // 4, d_state=d_state, ssm_ratio=expand, d_conv=d_conv)

        self.proj = nn.Linear(input_dim, output_dim)
        self.skip_scale = nn.Parameter(torch.ones(1))

    def forward(self, x, H, W):
        # x: B, N, C
        if x.dtype == torch.float16:
            x = x.type(torch.float32)
        B, N, C = x.shape
        x_norm = self.norm(x)

        # Channel Affinity
        z = x_norm.permute(0, 2, 1).mean(dim=2) # Global Avg Pool
        fc_out_1 = self.relu(self.fc1(z))
        fc_out_2 = self.sigmoid(self.fc2(fc_out_1))

        x_reshaped = rearrange(x_norm, 'b (h w) c -> b h w c', b=B, h=H, w=W, c=C)
        x1, x2, x3, x4 = torch.chunk(x_reshaped, 4, dim=-1)

        # 這裡需要 CrossScan 定義
        x_mamba1 = self.mamba_g1(x1, CrossScan=CrossScan_1, CrossMerge=CrossMerge_1)
        x_mamba2 = self.mamba_g2(x2, CrossScan=CrossScan_2, CrossMerge=CrossMerge_2)
        x_mamba3 = self.mamba_g3(x3, CrossScan=CrossScan_3, CrossMerge=CrossMerge_3)
        x_mamba4 = self.mamba_g4(x4, CrossScan=CrossScan_4, CrossMerge=CrossMerge_4)

        x_mamba = torch.cat([x_mamba1, x_mamba2, x_mamba3, x_mamba4], dim=-1) * self.skip_scale * x_reshaped
        x_mamba = rearrange(x_mamba, 'b h w c -> b (h w) c', b=B, h=H, w=W, c=C)

        x_mamba = x_mamba * fc_out_2.unsqueeze(1)
        x_mamba = self.norm(x_mamba)
        x_mamba = self.proj(x_mamba)
        return x_mamba

class PVT2FFN(nn.Module):
    def __init__(self, in_features, hidden_features):
        super().__init__()
        self.fc1 = nn.Linear(in_features, hidden_features)
        self.dwconv = DWConv(hidden_features)
        self.act = nn.GELU()
        self.fc2 = nn.Linear(hidden_features, in_features)

    def forward(self, x, H, W):
        x = self.fc1(x)
        x = self.dwconv(x, H, W)
        x = self.act(x)
        x = self.fc2(x)
        return x

class DWConv(nn.Module):
    def __init__(self, dim=768):
        super(DWConv, self).__init__()
        self.dwconv = nn.Conv2d(dim, dim, 3, 1, 1, bias=True, groups=dim)
    def forward(self, x, H, W):
        B, N, C = x.shape
        x = x.transpose(1, 2).view(B, C, H, W)
        x = self.dwconv(x)
        x = x.flatten(2).transpose(1, 2)
        return x

class GMBlock(nn.Module):
    """
    Standard GroupMamba Block.
    Args in YAML: [c2, mlp_ratio, d_state, d_conv, expand]
    """
    def __init__(self, c1, c2, mlp_ratio=4, d_state=16, d_conv=3, expand=1, drop_path=0.):
        super().__init__()
        # YOLO parse_model expects c2 to match c1 for standard blocks, 
        # but requires c2 in args to track channels.
        assert c1 == c2, f"Input channel {c1} != Output channel {c2} in GMBlock. Use GMDown for channel change."
        
        self.dim = c1
        self.norm2 = nn.LayerNorm(c1)
        self.attn = GroupMambaLayer(c1, c1, d_state=d_state, d_conv=d_conv, expand=expand)
        self.mlp = PVT2FFN(in_features=c1, hidden_features=int(c1 * mlp_ratio))
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()

    def forward(self, x):
        # Input x: (B, C, H, W)
        B, C, H, W = x.shape
        x_flat = x.flatten(2).transpose(1, 2) # (B, N, C)

        # Attention
        x_flat = x_flat + self.drop_path(self.attn(x_flat, H, W))
        # FFN
        x_flat = x_flat + self.drop_path(self.mlp(self.norm2(x_flat), H, W))

        # Restore to (B, C, H, W)
        out = x_flat.transpose(1, 2).view(B, C, H, W)
        return out